<script>
   function expandSection(element, section) {
    const container = document.querySelector('.sportsdev_mv_container');
    const boxes = document.querySelectorAll('.sportsdev_mv_box_mission, .sportsdev_mv_box_vision');
    
    boxes.forEach(box => {
        if (box === element) {
            box.classList.toggle('expanded');
            if (box.classList.contains('expanded')) {
                container.classList.add('has-expanded');
            } else {
                container.classList.remove('has-expanded');
            }
        } else {
            box.classList.remove('expanded');
        }
    });
}

function toggleServiceInfo(serviceId) {
    const box = document.querySelector(`#${serviceId}-info`).parentElement;
    const allBoxes = document.querySelectorAll('.sportsdev_services_box');

    // Close all other boxes before opening the current one
    allBoxes.forEach(otherBox => {
        if (otherBox !== box && otherBox.classList.contains('active')) {
            otherBox.classList.remove('active');
            otherBox.querySelector('.sportsdev_services_toggle').textContent = '+';
        }
    });

    // Toggle current box
    box.classList.toggle('active');
    const toggle = box.querySelector('.sportsdev_services_toggle');
    toggle.textContent = box.classList.contains('active') ? '×' : '+';
}

</script>