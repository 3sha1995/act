<script>
    function expandSection(element, section) {
        const container = document.querySelector('.health_mv_container');
        const boxes = document.querySelectorAll('.health_mv_box_mission, .health_mv_box_vision');
        
        boxes.forEach(box => {
            if (box === element) {
                box.classList.toggle('expanded');
                if (box.classList.contains('expanded')) {
                    container.classList.add('has-expanded');
                } else {
                    container.classList.remove('has-expanded');
                }
            } else {
                box.classList.remove('expanded');
            }
        });
    }

    function toggleServiceInfo(serviceId) {
        const box = document.querySelector(`#${serviceId}-info`).parentElement;
        const allBoxes = document.querySelectorAll('.student_affairs_services_box');

        // Close other expanded boxes
        allBoxes.forEach(otherBox => {
            if (otherBox !== box && otherBox.classList.contains('active')) {
                otherBox.classList.remove('active');
                otherBox.querySelector('.student_affairs_services_toggle').textContent = '+';
            }
        });

        // Toggle the clicked box
        box.classList.toggle('active');
        const toggle = box.querySelector('.student_affairs_services_toggle');
        toggle.textContent = box.classList.contains('active') ? '×' : '+';
    }

    // Add animation for facility, service, and appointment cards
    document.addEventListener('DOMContentLoaded', function() {
        // Animate facility cards
        const facilityCards = document.querySelectorAll('.health_facility_card');
        
        function animateFacilityCards() {
            facilityCards.forEach((card, index) => {
                const cardPosition = card.getBoundingClientRect().top;
                const screenPosition = window.innerHeight / 1.2;
                
                if(cardPosition < screenPosition) {
                    setTimeout(() => {
                        card.classList.add('animated');
                    }, index * 150); // Stagger animation
                }
            });
        }
        
        // Initial check for facility cards
        animateFacilityCards();
        
        // Animate service cards
        const serviceCards = document.querySelectorAll('.health_service_card');
        
        function animateServiceCards() {
            serviceCards.forEach((card, index) => {
                const cardPosition = card.getBoundingClientRect().top;
                const screenPosition = window.innerHeight / 1.2;
                
                if(cardPosition < screenPosition) {
                    setTimeout(() => {
                        card.classList.add('animated');
                    }, index * 150); // Stagger animation
                }
            });
        }
        
        // Initial check for service cards
        animateServiceCards();
        
        // Animate appointment cards
        const appointmentCards = document.querySelectorAll('.health_appointment_card');
        
        function animateAppointmentCards() {
            appointmentCards.forEach((card, index) => {
                const cardPosition = card.getBoundingClientRect().top;
                const screenPosition = window.innerHeight / 1.2;
                
                if(cardPosition < screenPosition) {
                    setTimeout(() => {
                        card.style.opacity = '1';
                    }, index * 150); // Stagger animation
                }
            });
        }
        
        // Initial check for appointment cards
        animateAppointmentCards();
        
        // Combined scroll event listener
        window.addEventListener('scroll', function() {
            animateFacilityCards();
            animateServiceCards();
            animateAppointmentCards();
        });
    });
</script>

